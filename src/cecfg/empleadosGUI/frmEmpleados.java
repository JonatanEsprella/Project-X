    public empleadosBL recuperarDatosGUI(){
        empleadosBL oempleados = new empleadosBL();
        
         oempleados.setCi(Ci.getText());
         oempleados.setNombre(Nombre.getText());
         oempleados.setApellidoP(ApellidoP.getText());
         oempleados.setApellidoM(ApellidoM.getText());
         oempleados.setNumero(Celular.getText());
         
         
         return oempleados;
         
    }
