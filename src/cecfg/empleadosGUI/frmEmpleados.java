    public void mostrarDatos(){
        conexion objConexion = new conexion();
        try {
            ResultSet resultado = objConexion.consultaRegistros("select * from datosPersonas");
            while (resultado.next()) {
                 System.out.println(resultado.getString("idDatos"));
                 System.out.println(resultado.getString("ci"));
                 System.out.println(resultado.getString("nombre"));
                 System.out.println(resultado.getString("apellidoP"));
                 System.out.println(resultado.getString("apellidoM"));
                 System.out.println(resultado.getString("numero"));
                 
                 Object[] usuario={resultado.getString("idDatos"),resultado.getString("ci"),resultado.getString("nombre"),resultado.getString("apellidoP"),resultado.getString("apellidoM"),resultado.getString("numero")};     
                 modelo.addRow(usuario);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
         
                
    }
    
